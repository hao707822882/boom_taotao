public class Good {
    private String name;
    private String price;
    private String recommend;
    private int id;
    public Good() {
    }

    public Good(String name, String price, String recommend, int id) {
        this.name = name;
        this.price = price;
        this.recommend = recommend;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getRecommend() {
        return recommend;
    }

    public void setRecommend(String recommend) {
        this.recommend = recommend;
    }


    public Good(String name, String price, String recommend) {
        this.name = name;
        this.price = price;
        this.recommend = recommend;
    }
}
